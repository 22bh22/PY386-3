def calc(a,b,c):
	x=0
	if c=="^" or c =="**":
		x=a**b
	elif c=="*":
		x=a*b
	elif c=="/":
		x=a/b
	elif c=="+":
		x=a+b
	elif c=="-":
		x=a-b
	return x
